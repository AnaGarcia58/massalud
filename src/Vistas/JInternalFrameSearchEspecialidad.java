    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed

       try {
           
           isEmptyField(jTextFieldId);
            String stringIdEspecialidad = jTextFieldId.getText();
            
            int idEspecialidad = Integer.parseInt(stringIdEspecialidad);

            boolean stateEspecialidad = jRadioButtonEstado.isSelected();

            especialidad = especialidadData.buscarEspecialidadPorId(idEspecialidad);
            
            jTextFieldNombre.setText(especialidad.getNombre());
            jRadioButtonEstado.setSelected(especialidad.isEstado());
            } catch (NullPointerException e) {
            visible(true);
            jLabelErrorEmptyField.setText("La especialidad no existe.");
        
        } catch (GenericException e) {
            visible(true);
            jLabelErrorEmptyField.setText(e.getMessage());
        }

    }//GEN-LAST:event_jButtonBuscarActionPerformed

