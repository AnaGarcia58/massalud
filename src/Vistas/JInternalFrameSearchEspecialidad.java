public void isEmptyField(JTextField textField) throws GenericException{
    if(textField.getText().isEmpty()||textField.getText().contains("Ej:")){
        throw new GenericException("El campo id no puede estar vacio.");
    }
    if(!contieneSoloNumeros(textField)){
        throw new GenericException("Solo se admiten numeros");
    }
}
