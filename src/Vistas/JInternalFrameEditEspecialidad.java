    private void jTextFieldNombreKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldNombreKeyPressed

        String nombre = jTextFieldNombre.getText();
        int longitudNombre = nombre.length();
        boolean isTrue;
        char teclaPresionada = evt.getKeyChar();
        int count = 0;

        if (teclaPresionada != KeyEvent.VK_BACK_SPACE) {

            nombre = jTextFieldNombre.getText() + teclaPresionada;
            longitudNombre = nombre.length();
            isTrue = nombre.equals(nombreActual) && longitudNombre == nombreActual.length();

        } else {
            //contador para que me permita contar la catidad que presiono la tecla
            //borrar
            count++;
            //variable longitudNombre la cual me sirve para verificar si tiene
            //la misma longitud el nombre acrual y el nombre modificado
            //A la cual le resto uno por que presiono la tecla eliminar
            longitudNombre = nombre.length() - 1;
            //reasigno a la variable nombre con el valor del TextField 
            //concatenado con las teclas presionadas.
            nombre = jTextFieldNombre.getText() + teclaPresionada;
            //armo un substring con lo que contiene la variable nombre 
            // y elimino de la ultima parte de la palabra las veces que preciono
            //la tecla borrar menos uno y de esta manera simulo que lo que escribio
            // y se borro en el textField sea lo correcto.
            nombre = nombre.substring(0, nombre.length() - 1 - count);
            //Por ultino reasigno a la variable isTrue con las respectivas evaluaciones
            isTrue = nombre.equals(nombreActual) && longitudNombre == nombreActual.length();

        }
        //si la variable isTrue es verdadera ejecuta el metodo disableButtonEdit()
        //caso contrario ejecuto el metodo enableButtonEdit()
        if (isTrue) {
            disableButtonEdit();

        } else {
            enableButtonEdit();

        }

    }//GEN-LAST:event_jTextFieldNombreKeyPressed

