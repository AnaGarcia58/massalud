    private void jComboBoxEspecialidadesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxEspecialidadesActionPerformed
        
        try{
            especialidad = (Especialidad)jComboBoxEspecialidades.getSelectedItem();
        
        nombreActual = especialidad.getNombre();
        estadoActual = especialidad.isEstado();
        
        jTextFieldNombre.setText(especialidad.getNombre());
        jRadioButtonEstado.setSelected(especialidad.isEstado());
        labelRadioButton();
         jLabelErrorEdit.setVisible(true);
         disableButtonEdit();
         jLabelErrorNull.setVisible(false);
         desactivarDesactivarCampos(true);
        }catch(NullPointerException e){
            
            jLabelErrorNull.setText("No ha seleccionado ninguna especialidad.");
            clearField();
            jLabelErrorNull.setVisible(true);
        }
    }//GEN-LAST:event_jComboBoxEspecialidadesActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        
        String nombreEspecialidad = jTextFieldNombre.getText();
        boolean estadoEspecialidad = jRadioButtonEstado.isSelected();
        try {
            validateField(nombreEspecialidad);
            
            especialidad = (Especialidad)jComboBoxEspecialidades.getSelectedItem();
        
        especialidad.setNombre(nombreEspecialidad);
        especialidad.setEstado(estadoEspecialidad);
        especialidadData.editarEspecialidad(especialidad);
        
        clearLabel();
        clearField();
        } catch (GenericException ex) {
            showLabelError(ex.getMessage());
        }
        
    }//GEN-LAST:event_jButtonEditarActionPerformed

