private void cargarComboBox(){
    jComboBoxEspecialidades.addItem(especialidad);
    for(Especialidad iterador : especialidadData.listaDeEspecialidades()){
        
        jComboBoxEspecialidades.addItem(iterador);
    
    }
}

private void desactivarDesactivarCampos(boolean isActivo){
    jTextFieldNombre.setEnabled(isActivo);
    jRadioButtonEstado.setEnabled(isActivo);
}

private void clearField(){
    jTextFieldNombre.setText("");
    jRadioButtonEstado.setSelected(false);
    jLabelRadioButton.setVisible(false);
    jButtonEditar.setEnabled(false);
    jComboBoxEspecialidades.setSelectedIndex(0);
    jLabelErrorEdit.setVisible(false);
    clearLabel();
}
private void clearLabel(){
        jLabelErrorEmptyField.setVisible(false);
        jLabelErrorNull.setVisible(false);
    }
private void validateField(String text) throws GenericException{
    
        if(text.isEmpty() || text.equals("Ej: Odontologia")){
            throw new GenericException("No ingrese campos vacios");
        }
        else if(!Utilidades.contieneSoloLetras(text)){
           
            throw new GenericException("No ingrese numero");
        }
}

private void showLabelError(String message){
        jLabelErrorEmptyField.setVisible(true);
        
        jLabelErrorEmptyField.setText(message);
        
}

private void labelRadioButton(){
    jLabelRadioButton.setVisible(true);
    
    if(jRadioButtonEstado.isSelected()){
        
        jLabelRadioButton.setForeground(new Color(0,102,0));
        jLabelRadioButton.setText("Activar la especialidad.");
    }else{
        jLabelRadioButton.setForeground(Color.RED);
        jLabelRadioButton.setText("Desactivar la especialidad.");
    }
}

private void disableButtonEdit(){
    jButtonEditar.setEnabled(false);
    jLabelErrorEdit.setForeground(Color.RED);
    jLabelErrorEdit.setText("El botón Editar se habilita si modifica algún campo");
}

private void enableButtonEdit(){
    jButtonEditar.setEnabled(true);
    jLabelErrorEdit.setText("");
}

